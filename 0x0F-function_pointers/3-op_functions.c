#include "3-calc.h"
/**
 * op_add - adds 2 integers
 * @a: first integer
 * @b: second integer
 * Return: Result
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - subtract 2 integers
 * @a: first integer
 * @b: second integer
 * Return: Result
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - product of 2 integers
 * @a: first integer
 * @b: second integer
 * Return: Result
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - division of 2 integers
 * @a: first integer
 * @b: second integer
 * Return: Result
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - modulo of 2 integers
 * @a: first integer
 * @b: second integer
 * Return: Result
 */
int op_mod(int a, int b)
{
	return (a % b);
}
